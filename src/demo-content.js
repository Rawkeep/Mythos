/**
 * Offline-saved content from previous generations.
 * Stored in localStorage under "mythos_saved_content".
 */
export function loadSavedContent() {
  try {
    return JSON.parse(localStorage.getItem("mythos_saved_content") || "[]");
  } catch {
    return [];
  }
}

export function saveContent(entry) {
  const saved = loadSavedContent();
  const updated = [{ ...entry, savedAt: Date.now() }, ...saved].slice(0, 50);
  localStorage.setItem("mythos_saved_content", JSON.stringify(updated));
  return updated;
}

export function deleteSavedContent(id) {
  const saved = loadSavedContent();
  const updated = saved.filter((s) => s.id !== id);
  localStorage.setItem("mythos_saved_content", JSON.stringify(updated));
  return updated;
}
